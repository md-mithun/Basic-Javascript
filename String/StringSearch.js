//Javascript String search

let text = "Lorem ipsum dolor sit amet consectetur adipisicing elit. Assumenda, aperiam";
console.log(text);

// ## String indexOf()
console.log(text.indexOf("ipsum"));//if find it will return index of specified string text otherwise return -1


// ## String search()
console.log(text.search("ipsum"));//same as indexOf()


// ## String includes()
console.log(text.includes("ipsum"));//if find=>return true ;else false


// ## String startsWith()
console.log(text.startsWith("Lorem"));//if start with specifiied text => return true ;else false


// ## String endsWith()
console.log(text.endsWith("aperiam"));//if start with specifiied text => return true ;else false



