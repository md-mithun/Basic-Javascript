
let num = 12.345;//(float)

// ## Numbers toString()
console.log(num.toString());//12.345(string)

// ## Number toExponential() method return a string with a number rounded and written using exponential notation
console.log(num.toExponential(3));//1.235e+1(string)

// ## Numbers toFixed()
console.log(num.toFixed(1));//12.3(string)

// ## Numbers toPrecision()
console.log(num.toPrecision(4));//12.35(string)